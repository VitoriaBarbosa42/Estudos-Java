package streams;

public class ClienteDesafioFilter {
	
	final String nome;
	final double totalCompra;
	final int numeroPedido;
	
	
	public ClienteDesafioFilter(String nome, double totalCompra, int numeroPedido) {
		this.nome = nome;
		this.totalCompra = totalCompra;
		this.numeroPedido = numeroPedido;
	}
	
	
}
